//Access modifiers

//Access modifiers can be used to state the access control level of a classes, attributes, methods and constructors
//There are only two different class definitions
//class Access - No definition (default)
//public class Access - global definitions (public)

//-----------CLASSES-----------\\

//public                    //The class is accessible by any other class
//*blank* class             //no definition (default would be replaced by nothing and just class by itself would be used

//-----------METHODS/VARIABLES/CONSTRUCTORS-----------\\

//public                    //the code is accessible by all classes
//private                   //the code is only accessible within the class its defined in
//*blank* class             //the code is only accessible within the package
//protected                 //the code is accessible in the same package and subclasses

